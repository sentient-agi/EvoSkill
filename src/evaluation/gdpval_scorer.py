def convert_to_pdf_in_docker(input_path: Path, output_dir: Path) -> Path:
    """Uses an ephemeral Docker container to convert office files to PDF safely."""
    # Get absolute paths for Docker volume mounting (Docker on Windows handles C:\... fine)
    abs_input_dir = input_path.parent.resolve()
    abs_output_dir = output_dir.resolve()
    file_name = input_path.name

    try:
        # Spin up container, mount the specific folders, run LibreOffice, and self-destruct (--rm)
        docker_cmd = [
            "docker", "run", 
            "--rm",
            "--network", "none",  # Sandbox: No internet access for the file
            "-v", f"{abs_input_dir}:/input:ro",   # Read-only input
            "-v", f"{abs_output_dir}:/output:rw", # Read-write output
            "gdpval-sandbox",
            "libreoffice", 
            "--headless", 
            "--convert-to", "pdf", 
            f"/input/{file_name}", 
            "--outdir", "/output"
        ]

        subprocess.run(
            docker_cmd, 
            check=True, 
            stdout=subprocess.DEVNULL, 
            stderr=subprocess.DEVNULL,
            timeout=60 # Prevent infinite hangs from malicious macros
        )
        
        expected_pdf = output_dir / f"{input_path.stem}.pdf"
        if expected_pdf.exists():
            return expected_pdf
        else:
            raise FileNotFoundError(f"PDF not generated by Docker sandbox for {file_name}")
            
    except subprocess.TimeoutExpired:
        print(f"Docker conversion timed out for {file_name}. Potential malicious file.")
        return input_path
    except subprocess.CalledProcessError as e:
        print(f"Docker sandbox conversion failed for {file_name}: {e}")
        return input_path


def convert_pdf_to_base64_images(pdf_path: Path, max_pages: int = 15) -> list[str]:
    """Converts a PDF into a list of base64-encoded PNG strings."""
    base64_images = []
    if pdf_path.suffix.lower() != '.pdf':
        return base64_images

    try:
        doc = fitz.open(pdf_path)
        for page_num in range(min(len(doc), max_pages)):
            page = doc.load_page(page_num)
            # Render at 150 DPI (matrix 2,2) for good LLM visibility
            pix = page.get_pixmap(matrix=fitz.Matrix(2, 2)) 
            img_bytes = pix.tobytes("png")
            b64_str = base64.b64encode(img_bytes).decode('utf-8')
            base64_images.append(b64_str)
        doc.close()
    except Exception as e:
        print(f"Error rendering PDF {pdf_path.name}: {e}")
    
    return base64_images


def extract_file_representations(file_path: Path) -> tuple[str, list[str]]:
    """Extracts both text content and visual base64 representations from a file."""
    # 1. Extract Text
    text_content = ""
    try:
        with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
            text_content = f.read()
            if len(text_content) > 10000:
                text_content = text_content[:10000] + "\n... [truncated]"
    except Exception:
        text_content = f"[Could not read text from {file_path.name}]"

    # 2. Extract Visuals (if applicable)
    visual_images = []
    ext = file_path.suffix.lower()
    visual_supported_exts = ['.pptx', '.docx', '.xlsx', '.pdf']
    
    if ext in visual_supported_exts:
        with tempfile.TemporaryDirectory() as temp_dir:
            temp_dir_path = Path(temp_dir)
            pdf_path = file_path
            
            if ext != '.pdf':
                pdf_path = convert_to_pdf_in_docker(file_path, temp_dir_path)
                
            if pdf_path.exists() and pdf_path.suffix.lower() == '.pdf':
                visual_images = convert_pdf_to_base64_images(pdf_path)

    return text_content, visual_images


def format_rubric(rubric_json: str | list) -> str:
    """Format rubric for the judge prompt."""
    if isinstance(rubric_json, str):
        try:
            rubric = json.loads(rubric_json)
        except json.JSONDecodeError:
            return rubric_json
    else:
        rubric = rubric_json
    
    formatted = []
    for item in rubric:
        if isinstance(item, dict):
            score = item.get('score', 0)
            criterion = item.get('criterion', '')
            formatted.append(f"[+{score}] {criterion}")
        else:
            formatted.append(str(item))
    
    return "\n".join(formatted)


def get_deliverable_content(
    task_id: str,
    generated_dir: Path,
    reference_deliverable_paths: list[str],
    gdpval_base_path: Path,
) -> tuple[str, list[str], str, list[str]]:
    """Get text and image representations of generated and reference deliverables."""
    generated_text_parts = []
    generated_images = []
    
    reference_text_parts = []
    reference_images = []
    
    # Process Generated Files
    if generated_dir.exists():
        for file_path in generated_dir.iterdir():
            if file_path.is_file():
                text, images = extract_file_representations(file_path)
                generated_text_parts.append(f"=== {file_path.name} ===\n{text}\n")
                generated_images.extend(images)
    
    # Process Reference Files
    for ref_path in reference_deliverable_paths:
        full_path = gdpval_base_path / ref_path
        if full_path.exists():
            text, images = extract_file_representations(full_path)
            reference_text_parts.append(f"=== {full_path.name} ===\n{text}\n")
            reference_images.extend(images)
    
    generated_text = "\n".join(generated_text_parts) if generated_text_parts else "[No generated files found]"
    reference_text = "\n".join(reference_text_parts) if reference_text_parts else "[No reference files found]"
    
    return generated_text, generated_images, reference_text, reference_images


async def score_gdpval_with_judge(
    task_id: str,
    prompt: str,
    rubric_json: str | list,
    generated_dir: Path,
    reference_deliverable_paths: list[str],
    gdpval_base_path: Path,
    judge_agent: Optional[Agent] = None,
) -> tuple[float, str]:
    """Score GDPval task using multimodal LLM-as-judge."""
    
    # Get multimodal file contents
    gen_text, gen_images, ref_text, ref_images = get_deliverable_content(
        task_id, generated_dir, reference_deliverable_paths, gdpval_base_path
    )
    
    rubric_formatted = format_rubric(rubric_json)
    
    # Build the text prompt
    text_prompt = f"""You are an expert evaluator comparing deliverable files against a rubric.

## Original Task Prompt
{prompt}

## Scoring Rubric
{rubric_formatted}

## Deliverable A (Agent Generated)
Text Content:
{gen_text}

## Deliverable B (Human Reference)
Text Content:
{ref_text}

## Instructions
Compare two deliverables using the rubric above. Evaluate both text correctness AND visual formatting/aesthetics.

Pick the better deliverable, or declare a tie.

Provide your response exactly in this format on separate lines:
BETTER DELIVERABLE: <A, B, or Tie>
RATIONALE: <explanation>"""
    
    # Build image content blocks for multimodal scoring
    image_blocks: list[dict] = []
    
    # Add generated deliverable images (with label text)
    if gen_images:
        for i, b64_img in enumerate(gen_images):
            label_block: dict = {"type": "text", "text": f"\n[Generated deliverable image {i+1}]:"}
            image_blocks.append(label_block)
            image_block: dict = {
                "type": "image_url",
                "image_url": {"url": f"data:image/png;base64,{b64_img}"}
            }
            image_blocks.append(image_block)
    
    # Add reference deliverable images (with label text)
    if ref_images:
        for i, b64_img in enumerate(ref_images):
            label_block: dict = {"type": "text", "text": f"\n[Reference deliverable image {i+1}]:"}
            image_blocks.append(label_block)
            image_block: dict = {
                "type": "image_url",
                "image_url": {"url": f"data:image/png;base64,{b64_img}"}
            }
            image_blocks.append(image_block)
    
    # Use dspy with gemini-2.5-flash (same as sealqa scorer) for multimodal judging
    try:
        import os
        import dspy
        
        openrouter_key = os.environ.get("OPENROUTER_API_KEY")
        if openrouter_key:
            lm = dspy.LM("openrouter/google/gemini-2.5-flash", api_key=openrouter_key)
        else:
            raise RuntimeError("No scorer API key found. Set OPENROUTER_API_KEY in .env")
        
        # Build messages with text and images for multimodal scoring
        messages = [{"role": "user", "content": []}]
        
        # Add text content
        messages[0]["content"].append({
            "type": "text",
            "text": text_prompt
        })
        
        # Add image content blocks
        for block in image_blocks:
            if block.get("type") == "image_url":
                image_url = block.get("image_url", {})
                url = image_url.get("url", "")
                if url.startswith("data:image/png;base64,"):
                    # Extract base64 data
                    base64_data = url.replace("data:image/png;base64,", "")
                    messages[0]["content"].append({
                        "type": "image_url",
                        "image_url": {"url": f"data:image/png;base64,{base64_data}"}
                    })
        
        # Use dspy with the multimodal content
        grader = dspy.ChainOfThought("messages:list -> score_and_rationale:str")
        
        with dspy.context(lm=lm):
            response = grader(messages=messages)
        
        result_text = response.score_and_rationale
        
        score = 0.0
        rationale = result_text
        
        # Parse the decision
        lines = result_text.strip().split('\n')
        for line in lines:
            if line.upper().startswith('BETTER DELIVERABLE:'):
                decision = line.split(':', 1)[1].strip().upper()
                if 'A' in decision:
                    score = 1.0  # Agent wins
                elif 'B' in decision:
                    score = 0.0  # Human wins
                elif 'TIE' in decision:
                    score = 0.5  # Tie
            elif line.upper().startswith('RATIONALE:'):
                rationale = line.split(':', 1)[1].strip()
                
        return score, rationale
        
    except Exception as e:
        return 0.0, f"Judge evaluation failed: {str(e)}"


